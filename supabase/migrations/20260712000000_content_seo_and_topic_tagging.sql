-- ============================================================
-- ALTER TABLE: content_items
-- Adds the three SEO-build columns: published_url (unique, for idempotent
-- backfill), seo_title (distinct from the on-page `title`), and faq_schema
-- (FAQPage JSON-LD, built server-side).
-- ============================================================

ALTER TABLE content_items
  ADD COLUMN published_url TEXT,     -- live URL; set once via "Mark as Published"; NULL until then
  ADD COLUMN seo_title     TEXT,     -- ~55-60 char literal/keyword-forward title, distinct from `title`
  ADD COLUMN faq_schema    JSONB;    -- FAQPage schema.org JSON-LD object; NULL if not applicable

ALTER TABLE content_items
  ADD CONSTRAINT uq_content_items_published_url UNIQUE (published_url);

COMMENT ON COLUMN content_items.published_url IS
  'Live URL, set once by Peter via "Mark as Published" after the post actually goes live. UNIQUE (multiple NULLs allowed) so the historical backfill script can check for an existing row by URL before inserting, making it safe to re-run.';

COMMENT ON COLUMN content_items.seo_title IS
  'Short (~55-60 char), literal, keyword-forward title for search engines. Generated by the AI on every new draft and revision. Distinct from `title`, which stays the on-page headline in Peter''s established voice.';

COMMENT ON COLUMN content_items.faq_schema IS
  'FAQPage schema.org JSON-LD object, built server-side (JSON.stringify on a real object). NULL when not applicable to this content item.';

COMMENT ON COLUMN content_items.body IS
  'Draft/final content. For normally-drafted items this is the full long-form text. For the 185 backfilled historical posts, this deliberately holds only a short 1-3 sentence summary instead of full article text — those rows are internal-link targets only, never a source of facts for the AI, and keeping `body` short on those rows keeps that boundary visible just by looking at the data. No column-level length constraint either way.';


-- ============================================================
-- TABLE: content_item_topics
-- What it stores: which of the 12 compliance_topics a content item is
-- about — for both new AI-drafted posts and the 185 backfilled historical
-- posts, so internal-linking match logic works identically for old and new
-- content. This is NOT the same relationship as
-- content_item_compliance_claims: that table means "this piece cited this
-- specific legal claim as grounding." This table only means "this piece is
-- about this topic" — backfilled posts get rows here but never in
-- content_item_compliance_claims, since they cite no claims.
-- RLS: enabled, locked by default.
-- ============================================================

CREATE TABLE content_item_topics (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  content_item_id   UUID NOT NULL REFERENCES content_items(id) ON DELETE CASCADE,
  topic_id          UUID NOT NULL REFERENCES compliance_topics(id) ON DELETE RESTRICT,
  created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  CONSTRAINT uq_content_item_topic UNIQUE (content_item_id, topic_id)
);

ALTER TABLE content_item_topics ENABLE ROW LEVEL SECURITY;

CREATE INDEX idx_content_item_topics_content_item_id ON content_item_topics(content_item_id);
CREATE INDEX idx_content_item_topics_topic_id        ON content_item_topics(topic_id);


