--   1. content_items.published_url — the live URL, set once by Peter via a
--      "Mark as Published" action after a post actually goes live. UNIQUE so
--      the upcoming 185-post historical backfill script can safely check
--      "is this URL already in the table?" before inserting — making the
--      backfill idempotent (safe to re-run, and safe to run alongside new
--      posts being marked published) without ever creating a duplicate row
--      for the same URL.
--   2. content_items.seo_title — a short (~55-60 char), literal,
--      keyword-forward title, generated by the AI on every new draft and
--      every revision. Distinct from the existing `title` column, which
--      stays the punchy on-page headline in Peter's established voice.
--      Mirrors how `meta_description` already works on this table.
--   3. content_items.faq_schema — a FAQPage schema.org JSON-LD object,
--      built server-side by the app (Q's concern, not this migration's).
--   4. content_item_topics (new table) — many-to-many link between a
--      content item and the compliance_topics it's about (12 real legal
--      topics). Populated for BOTH new AI-drafted posts and the 185
--      backfilled historical posts, so internal-linking logic
--      (getInternalLinkCandidates()-style: "find items sharing a topic")
--      works identically for old and new content. Deliberately separate
--      from content_item_compliance_claims, which means something stronger
--      ("this piece cited this specific legal claim as grounding") —
--      backfilled posts are link targets only and must never be mistaken
--      for a grounding source.
--
-- Design notes:
--   - published_url is nullable (most rows haven't been published yet) but
--     UNIQUE — Postgres allows unlimited NULLs under a UNIQUE constraint,
--     so this is safe for the many draft/unpublished rows.
--   - content_item_topics is a plain join table (content_item_id, topic_id),
--     matching this schema's existing pattern for many-to-many links —
--     content_item_compliance_claims (in 20260710000000) is the direct
--     precedent: same shape, same ON DELETE behavior (CASCADE on the
--     content_item side, RESTRICT on the referenced-topic side so a topic
--     in use can't be silently deleted out from under linked content). A
--     TEXT[] array of topic_keys directly on content_items was considered
--     and rejected: it would drop referential integrity (no guarantee a
--     stored key still matches a real compliance_topics row), it would be
--     the only place in this schema doing many-to-many that way, and the
--     query Q needs — "every content_item sharing at least one topic with
--     a given set of topic_ids" — is a plain indexed join against this
--     table (`WHERE topic_id IN (...)`), no less simple than an array
--     overlap (`&&`) query would have been.
--   - No new column was added for the backfilled posts' short 1-3 sentence
--     summary. The existing `content_items.body` column is reused as-is:
--     it has no length constraint, and every code path that reads it
--     (projects/content-review/server.js's draft-detail view via
--     lib/highlight.js's renderDraftBody()/countReviewFlags(), and
--     content-engine's lib/draft.js and lib/revise.js) either just
--     displays whatever text is there or regex-scans it for a
--     "[NEEDS HUMAN REVIEW: ...]" flag — nothing assumes `body` is always
--     full-length. revise.js in particular (the one place `body` feeds
--     back into an AI call) only runs against items whose status is
--     'needs_changes', which backfilled rows — inserted directly with
--     status='published' — will never pass through. A COMMENT ON COLUMN is
--     added below to document the dual meaning for future readers.
--
-- RLS is enabled on the new table and locked down by default, matching
-- every other table in this schema — no permissive policy is added here.
--
